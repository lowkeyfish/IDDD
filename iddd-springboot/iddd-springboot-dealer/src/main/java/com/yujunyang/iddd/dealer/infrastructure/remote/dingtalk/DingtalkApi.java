package com.yujunyang.iddd.dealer.infrastructure.remote.dingtalk;

import org.springframework.cloud.openfeign.FeignClient;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.RequestBody;
import org.springframework.web.bind.annotation.RequestParam;

@FeignClient(name = "api-dingtalk")
public interface DingtalkApi {
    @PostMapping("/robot/send")
    RobotSendResponse robotSend(
            @RequestParam("access_token") String accessToken,
            @RequestParam("timestamp") long timestamp,
            @RequestParam("sign") String sign,
            @RequestBody RobotSendRequestBody requestBody);
}
